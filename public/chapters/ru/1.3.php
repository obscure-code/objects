    <h2>Подпространства</h2>

    <p>Пространства можно расширять, дополняя определенным набором воздействий или наоборот — сужать, запрещая часть
        воздействий, таким образом выделяя подпространства.</p>
    <p>Например, в общем информационном пространстве можно выделить подпространства звуков, вкусов, визуальных образов. Эти
