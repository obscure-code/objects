    <h2>Другие пространства</h2>

    <p>Водитель автомобиля находится в пространстве дорожного движения, в котором определены специальные информационные
        воздействия. Например, правила обязывают показывать поворотниками направление движения. Также для воздействия на
        других водителей используют дальний свет, звуковой сигнал, аварийную сигнализацию.</p>
    <p>Еще одно производное пространство — юридическое. Когда водитель составляет с инспектором ГАИ протокол, они
        взаимодействуют в юридическом пространстве. Внешними воздействиями являются установленные юридические процедуры:
        штраф, арест имущества, лишение свободы, которые могут быть организованы в торговом или физическом пространстве.</p>
    <p>Термин «пространство» часто встречается в математике. Некоторые математические пространства можно описать на
        языке объектов.</p>

    <h3>Векторное пространство</h3>
    <p>Рассмотрим объекты, с которыми можно производить изменения: складывать с другими объектами и умножать на числа.
        Если в результате этих операций получаются объекты такого же типа, и эти операции подчиняются определенным аксиомам,
        то перед нами важная математическая структура — линейное или векторное пространство. Объекты такого пространства
        называются векторами.</p>

    <p>Люди пытаются уйти от несправедливых взаимодействий физического и торгового пространств в особые духовные
        пространства. Существует большое разнообразие таких пространств — от канонических религий до различных
        эзотерических практик. Считается, что в этих пространствах объекты высокого уровня продолжают существовать после смерти.</p>
