        изменения. Такие объекты будем называть «точками пространства» (в этой главе под словом «пространство»
        подразумевается множество элементарных объектов). Вопрос об их внутреннем устройстве остается открытым.
        Например, это могут быть объекты с бинарным состоянием 0 или 1.</p>

    <p>Теперь покажем, как из таких элементарных объектов можно получить более сложные. Изобразим на плоскости несколько
